function renderMarkdownSlice(payload) {
  const lines = [];
  lines.push(`# ${titleCase(payload.command)}`);
  lines.push('');
  if (payload.scope.length > 0) {
    lines.push('## Scope');
    for (const item of payload.scope) {
      lines.push(`- ${item}`);
    }
    lines.push('');
  }
  if (payload.hypothesis) {
    lines.push('## Hypothesis');
    lines.push(payload.hypothesis);
    lines.push('');
  }
  if (payload.reason) {
    lines.push(`Reason: ${payload.reason}`);
  }
  return lines.join('\n').trim();
}

