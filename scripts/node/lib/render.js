'use strict';

function titleCase(text) {
  return String(text)
    .split(/[\s._-]+/)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}

function renderKeyValueList(entries) {
  return entries
    .map(([key, value]) => `${key}: ${typeof value === 'string' ? value : JSON.stringify(value)}`)
    .join('\n');
}

function renderMarkdownHelp(payload) {
  const lines = [];
  lines.push(`# ${payload.title || 'Workflow CLI'}`);
  if (payload.summary) {
    lines.push('');
    lines.push(payload.summary);
  }
  lines.push('');
  lines.push('## Commands');
  for (const command of payload.commands) {
    lines.push(`- \`${command.name}\`${command.description ? `: ${command.description}` : ''}`);
  }
  return lines.join('\n');
}

function renderMarkdownStatus(payload) {
  const lines = [];
  lines.push('# Workflow Status');
  lines.push('');
  lines.push(`- Session status: ${payload.session.session.status}`);
  lines.push(`- Current state: ${payload.session.session.current_state}`);
  lines.push(`- Manifest: ${payload.manifest.name || 'unknown'}`);
  lines.push(`- Next: ${payload.suggestion.command}`);
  lines.push(`- Reason: ${payload.suggestion.reason}`);
  lines.push(
    `- Active lane: ${payload.session.session.active_lane || 'unassigned'}`,
  );
  lines.push(
    `- Active scope: ${(payload.session.session.writable_scope || []).join(', ') || 'n/a'}`,
  );
  lines.push(
    `- Hypothesis: ${payload.session.session.hypothesis || 'n/a'}`,
  );
  if (renderNextCandidateLine(payload.session.next_step)) {
    lines.push(
      `- Next candidate: ${renderNextCandidateLine(payload.session.next_step)}`,
    );
  }
  return lines.join('\n');
}

