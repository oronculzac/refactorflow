function renderMarkdownChecklist(payload) {
  const lines = [];
  lines.push('# Precommit Checklist');
  lines.push('');
  for (const item of payload.items) {
    lines.push(`- [${item.done ? 'x' : ' '}] ${item.text}`);
  }
  if (payload.notes && payload.notes.length > 0) {
    lines.push('');
    lines.push('## Notes');
    for (const note of payload.notes) {
      lines.push(`- ${note}`);
    }
  }
  return lines.join('\n');
}

function renderPlain(payload) {
  if (payload.markdown) {
    return payload.markdown;
  }
  return renderKeyValueList(Object.entries(payload));
}

function renderNextCandidateLine(nextCandidate) {
  if (!nextCandidate || typeof nextCandidate !== 'object') {
    return null;
  }
  return `${nextCandidate.candidate} (${nextCandidate.status})`;
}

